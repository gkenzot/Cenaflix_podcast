package br.com.senac.cenaflix_podcast.login;

/**
 * Representa um usuário do sistema.
 * 
 * Esta classe encapsula as informações de um usuário, incluindo seu ID, nome, login, senha e tipo.
 * 
 * Fornece métodos para acessar e modificar essas informações. (Sets and Gets)
 * 
 * @author kenzo
 */
public class Usuario {

    private int id;
    private String nome;
    private String login;
    private String senha;
    private String tipo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
